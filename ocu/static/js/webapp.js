$(document).ready(function(){

    $(".activitiesNav").mouseover(function(){
        $(".activities-nav").show();
    });

    $(".activitiesNav").mouseout(function(){
        $(".activities-nav").hide();
    });

    $(".programNav").mouseover(function(){
        $(".program-nav").show();
    });

    $(".programNav").mouseout(function(){
        $(".program-nav").hide();
    });

});
